def my_function():
    """
    A simple function that returns a greeting.
    """
    return "Hello from my function!"

def main():
    """
    Función principal para 015_function_definition
    """
    # Ejemplo de uso
    result = my_function()
    print(f"Función dice: {result}")
    return result

if __name__ == "__main__":
    result = main()
    print(f"Resultado: {result}")
