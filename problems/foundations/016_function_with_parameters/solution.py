"""
Solution for Function with Parameters
Problem ID: F016
"""

def add(a, b):
    """
    Adds two numbers and returns the result.
    """
    return a + b

def main():
    """
    Funci√≥n principal para 016_function_with_parameters
    """
    # Ejemplo de uso
    result = add(3, 5)
    print(f"3 + 5 = {result}")
    return result

if __name__ == "__main__":
    result = main()
    print(f"Resultado: {result}")
